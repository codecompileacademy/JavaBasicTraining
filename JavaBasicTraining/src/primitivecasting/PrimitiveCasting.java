package primitivecasting;

public class PrimitiveCasting {

	public static void main(String[] args) {
		
		byte a = 10;
		
	    int b = a; // implicit casting
	    
	    int p= 10;
	    byte q =(byte) p; // explicit casting
		
	    byte x =(byte) 130;
	    
	    float f = 15.5f;
	    
	    System.out.println("x :" + x);
	}

}
